void main()
{
	if(1 == 1) { printf("OK: =="); }
	else { printf("ERROR: =="); }

	if(1 <= 1) { printf("OK: <="); }
	else { printf("ERROR: <="); }

	if(1 >= 1) { printf("OK: >="); }
	else { printf("ERROR: >="); }

	if(1 != 1) { printf("ERROR: !="); }
	else { printf("OK: !="); }

	if(1 < 1) { printf("ERROR: <"); }
	else { printf("OK: <"); }

	if(1 > 1) { printf("ERROR: >"); }
	else { printf("OK: >"); }
}